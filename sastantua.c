#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	if	(*str != '\0')
	{
		ft_putchar(*str);
		str++;
		ft_putstr(str);
	}
}

int 	main(int argc, char **argv)
{

	argv++;
	if	(**argv != '\0')
	{
		argv++;
	}

	ft_putstr("  /*\\ ");
	ft_putchar('\n');
	ft_putstr(" /***\\ ");
	ft_putchar('\n');
	ft_putstr("/**|**\\");
	ft_putchar('\n');

	return 0;

}
